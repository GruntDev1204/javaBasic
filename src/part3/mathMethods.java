package part3;

public class mathMethods {
    public static double Cong(double a,double b){
        return  a+b;
    }

    public static double Tru(double a,double b){
        return  a-b;
    }

    public static double nhan(double a,double b){
        return  a*b;
    }

    public static double chia(double a,double b){
        return  a/b;
    }
}
